import Property from '../comps/Property'
import { baseUrl, fetchApi } from '../utils/fetchApi'
import Head from 'next/head'

const RentProperty = ({propertiesForRent}) => {
    return ( 
        <>
        <Head>
        <title>Real Estate | Rent Property</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
        <div>
            <div className="rent-heading">
                <h1>Property for Rent</h1>
                <p>Rent a home</p>
            </div>
            <div className="card-container">{propertiesForRent && propertiesForRent.map((property)=>{
            return <Property property={property} key={property.id}/> 
            })}</div>
        </div>
        </>
     );
}
 
export default RentProperty;

export async function getStaticProps(){
    const propertyForRent = await fetchApi(`${baseUrl}/properties/list?locationExternalIDs=5002&purpose=for-rent`)
  
    return {
      props:{
        propertiesForRent:propertyForRent?.hits
      }
    }
  }